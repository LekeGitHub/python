import math

print("ceil(4.4) = ", math.ceil(2.1))
print("floor(4.4) = ", math.floor(4.4))
print("fabs(-4.4) = ", math.fabs(-4.4))

# Factorial = 1 * 2 * 3 * 4
print("factorial(4) = ", math.fabs(4))

# Return remainder of division
print("fmod(5,4)", math.fmod(5,4))


# Recieve a float and return an int
print("truc(4.2) = ", math.trunc(4.2))

# Return the Square root
print("Sqrt(4) = ", math.sqrt(4))

num = int(input("Enter a num : "))

# Return the Square root
print("Sqrt of {} = ", format(num), math.sqrt(num))


